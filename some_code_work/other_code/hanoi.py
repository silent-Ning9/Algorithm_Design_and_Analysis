"""
n阶汉诺塔（Hanoi）问题

X, Y, Z是三个塔座，开始时有 n 个盘子依其半径大小套在柱子 X 上，其中半径大的在下面。
现要将 X 上的圆盘移到 Z 上，并仍按同样顺序叠置。

移动规则：
① 每次只能移动1个圆盘；
② 任何时刻都不允许将半径大的圆盘压在半径小的圆盘之上；
"""


def hanoi(n, X, Y, Z):
    """
    解决n阶汉诺塔问题
    参数:
    n: 盘子数量
    X: 起始柱子名称
    Y: 辅助柱子名称
    Z: 目标柱子名称
    """
    if n == 1:
        print(f"移动 {X} -> {Z}")
    else:
        hanoi(n-1, X, Z, Y)  # 将n-1个盘子从X移到Y，使用Z作为辅助
        print(f"移动 {X} -> {Z}")  # 将最大的盘子从X移到Z
        hanoi(n-1, Y, X, Z)  # 将n-1个盘子从Y移到Z，使用X作为辅助

# 测试示例
if __name__ == "__main__":
    n = 3  # 可以修改这个值来测试不同数量的盘子
    print(f"{n}阶汉诺塔解决方案:")
    hanoi(n, 'A', 'B', 'C')